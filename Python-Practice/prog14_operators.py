
# Binary Operators
print( 21 & 13)
# & = Binary AND
# | = Binary OR

print("0 & 0 in binary = ", 0 & 0)
print("0 & 1 in binary = ", 0 & 1)
print("1 & 0 in binary = ", 1 & 0)
print("1 & 1 in binary = ", 1 & 1)

print("0 | 0 in binary = ", 0 | 0)
print("0 | 1 in binary = ", 0 | 1)
print("1 | 0 in binary = ", 1 | 0)
print("1 | 1 in binary = ", 1 | 1)
